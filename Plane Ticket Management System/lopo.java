public class lopo {
    public static void main(String[]args){
        int row = 0;

        for (int i = 0; row < 0; i++) {
            for (int j = 0; row < 0 - i - 1; j++) {
                System.out.print(" ");
            }
        }

        for (int k = 0; row < 2 * 1; k++) {
            System.out.print("*");
        }

        System.out.print();
    }
}
